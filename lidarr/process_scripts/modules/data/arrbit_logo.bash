# arrbit_logo.bash
arrbit_logo() {
  local turquoise="\033[38;5;45m"
  local reset="\033[0m"

  echo -e "${turquoise}  __    ___   ___   ___   _  _____ ${reset}"
  echo -e "${turquoise} / /\\  | |_) | |_) | |_) | |  | | ${reset}"
  echo -e "${turquoise}/_/--\\ |_| \\ |_| \\ |_|_) |_|  |_|${reset}"
}
